	<h1>Qu'est-ce que TeamShare ?</h1>

	<p>TeamShare est une application web de gestion de tâches.</br> 
	Cette application permet de créer des tâches, de les afficher sur un calendrier.</br>
	Une interface permet également de visualiser les tâches sous forme de post-it.</br>
	Cette interface possède un système drag and drop qui permet d'organiser les tâches.
	</p>

	<p>Pour utiliser l'application, il suffit de s'incrire en cliquant sur l'onglet "s'incrire".</br>
		Une fois tous les champs renseignés, vous pouvez vous connecter à l'aide de votre login et de votre mot de passe en cliquant sur l'onglet "se connecter".</br>
		Vous avez désormais accès à l'onglet "gestion des tâches" qui vous permet de visualiser toutes vos tâches et de créer une nouvelle tâche.</br>
		Cette tâche nouvellement crée va apparaitre dans le calendrier de la page d'accueil.</br>
		Pour obtenir le détail d'une tâche, il suffit de cliquer dans le calendrier, sur la date de votre choix.</br>
		Vous aurez ainsi accès au détail des tâches de la journée</br>
		Si vous cliquez sur une des tâches de cette page, vous aurez la possibilité de vous inscrire à une tâche.
	</p>



